int fputc(int ch, FILE *stream)
{
    /* 堵塞判断串口是否发送完成 */
    while((USART2->SR & 0X40) == 0);
 
    /* 串口发送完成，将该字符发送 */
    USART2->DR = (uint8_t) ch;
 
    return ch;
}

