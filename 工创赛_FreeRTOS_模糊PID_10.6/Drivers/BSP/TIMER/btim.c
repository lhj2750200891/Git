/**
 * @brief       定时器底册驱动，开启时钟，设置中断优先级
                此函数会被HAL_TIM_Base_Init()函数调用
 * @param       无
 * @retval      无
 */
void HAL_TIM_Base_MspInit(TIM_HandleTypeDef *htim)
{
    if (htim->Instance == BTIM_TIM7_INT)
    {
        BTIM_TIM7_INT_CLK_ENABLE();                     /*使能TIM时钟*/
        HAL_NVIC_SetPriority(BTIM_TIM7_INT_IRQn, 1, 0); /* 抢占4，子优先级0，组4 */
        HAL_NVIC_EnableIRQ(BTIM_TIM7_INT_IRQn);         /*开启ITM3中断*/
    }
		  else if(htim->Instance==BTIM_TIM6_INT)
  {
				BTIM_TIM6_INT_CLK_ENABLE();
				HAL_NVIC_SetPriority(BTIM_TIM6_INT_IRQn, 2, 0);
				HAL_NVIC_EnableIRQ(BTIM_TIM6_INT_IRQn);
  }
}



